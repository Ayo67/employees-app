        const val WHITE = "\u001b[37m"

        const val BOLD =  "\u001b[1m"
        const val UNDERLINE ="\u001b[4m"

        const val BLACK_UNDERLINED = "\u001b[4;30m"
        const val RED_UNDERLINED = "\u001b[4;31m"
        const val GREEN_UNDERLINED = "\u001b[4;32m"
